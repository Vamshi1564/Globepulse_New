    <!-- JS: Count-up when visible + subtle entrance fade -->
    <script>
        (function () {
            const cards = document.querySelectorAll('.stats-card');
            const counters = document.querySelectorAll('.stats-number');

            // Add fade-up entrance for each card
            cards.forEach((c, i) => {
                c.style.opacity = 0;
                c.style.transform = 'translateY(18px)';
                setTimeout(() => {
                    c.style.transition = 'opacity .6s ease, transform .6s cubic-bezier(.2,.9,.2,1)';
                    c.style.opacity = 1;
                    c.style.transform = 'translateY(0)';
                }, 120 + i * 90);
            });

            // Count-up runner
            function animateCount(el, target) {
                const start = 0;
                const duration = 1400; // ms
                const stepTime = Math.max(16, Math.floor(duration / Math.max(target, 1)));
                let current = start;
                const increment = Math.max(1, Math.round(target / (duration / stepTime)));
                const timer = setInterval(() => {
                    current += increment;
                    if (current >= target) {
                        el.textContent = (target >= 1000) ? `${target}+` : target;
                        clearInterval(timer);
                        return;
                    }
                    el.textContent = current;
                }, stepTime);
            }

            // IntersectionObserver to trigger once per counter
            if ('IntersectionObserver' in window) {
                const observer = new IntersectionObserver((entries, obs) => {
                    entries.forEach(entry => {
                        if (entry.isIntersecting) {
                            const el = entry.target;
                            const target = parseInt(el.getAttribute('data-count'), 10) || 0;
                            if (!el.dataset.started) {
                                animateCount(el, target);
                                el.dataset.started = 'true';
                            }
                            obs.unobserve(el);
                        }
                    });
                }, { threshold: 0.6 });

                counters.forEach(c => observer.observe(c));
            } else {
                // fallback if IntersectionObserver not supported
                counters.forEach(c => animateCount(c, parseInt(c.getAttribute('data-count'), 10) || 0));
            }

            // Accessibility: ensure reduced-motion respects preference
            const mq = window.matchMedia('(prefers-reduced-motion: reduce)');
            if (mq && mq.matches) {
                // remove transitions and instant set numbers
                cards.forEach(c => { c.style.transition = 'none'; c.style.transform = ''; c.style.opacity = 1; });
                counters.forEach(c => { c.textContent = c.getAttribute('data-count'); });
            }
        })();
    </script>



